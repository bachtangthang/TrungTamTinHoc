create procedure SET_IDGV_OF_LOPHP
as
	declare @cur cursor
	declare @id_lop int
	declare @idmonhoc int
	declare @idgiaovien int
begin
	set @cur = CURSOR for 
		select ID_Lop, ID_MonHoc, ID_GiaoVien from LOPHOCPHAN 
		open @cur
		fetch next from @cur
		into @id_lop, @idmonhoc, @idgiaovien
	
	while @@FETCH_STATUS = 0
	begin
		update LOPHOCPHAN
		set ID_GiaoVien = (select top 1 ID_GiaoVien from GIAOVIEN_MONHOC gvmh where gvmh.ID_MonHoc = @idmonhoc order by newid())
		where ID_Lop = @id_lop

		fetch next from @cur
		into @id_lop, @idmonhoc, @idgiaovien
	end
	close @cur
	deallocate @cur
end

exec SET_IDGV_OF_LOPHP;

